package com.apache.exception;

public class NoSuchCipherException extends Exception {

	  public NoSuchCipherException() { super(); }
	  public NoSuchCipherException(String message) { super(message); }
	  public NoSuchCipherException(String message, Throwable cause) { super(message, cause); }
	  public NoSuchCipherException(Throwable cause) { super(cause); }
	
}

