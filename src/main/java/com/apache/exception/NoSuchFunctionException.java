package com.apache.exception;

public class NoSuchFunctionException extends Exception {

	  public NoSuchFunctionException() { super(); }
	  public NoSuchFunctionException(String message) { super(message); }
	  public NoSuchFunctionException(String message, Throwable cause) { super(message, cause); }
	  public NoSuchFunctionException(Throwable cause) { super(cause); }
	
}


